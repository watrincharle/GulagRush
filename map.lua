function map:load()
    map.road = love.graphics.newImage("sprites/road.png")
    map.wall = love.graphics.newImage("sprites/wall.png")
    map.tileSize = 64
    map.col = 40
    map.row = 50
    map.width = (map.tileSize * map.row) 
    map.height = (map.tileSize * map.col) 
    map.posX = love.graphics.getWidth()/2 - map.width/2 + map.tileSize * 2
    map.posY = love.graphics.getHeight()/2 - map.height/2 + map.tileSize * 2
    map.hitbox = 64
end



function map:update()

end

function map:draw()
    for y = 1, #mapData do
        for x = 1, #mapData[y] do
            if mapData[y][x] == 2 then
                love.graphics.draw(map.wall, map.posX + (x * map.tileSize), map.posY + (y * map.tileSize), 0)
            else
                love.graphics.draw(map.road, map.posX + (x * map.tileSize), map.posY + (y * map.tileSize), 0)
            end
            
        end
    end
end


function map.init()
    map.posX = love.graphics.getWidth()/2 - map.width/2 
    map.posY = love.graphics.getHeight()/2 - map.height/2
end 

return map
